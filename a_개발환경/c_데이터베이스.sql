	> 데이터를 효율적으로 관리 & 처리

# 데이터베이스 주요 개념
1. 스키마(Schema)
	== 데이터베이스, DB의 구조와 제약조건을 정의한 것
    > 테이블, 필드, 데이터 타입, 관계 등을 포함
    
2. 테이블(Table)
: DB(스키마)의 가장 기본적인 저장 단위
- 행과 열의 격자 형태로 데이터를 구조화하여 저장 (표 형식)
- 테이블 구조
	> 필드(열), 레코드(행), 키(테이블 내의 행을 식별하는 데 사용하는 열)
    
# 필드(Field, 속성)
: 테이블의 열을 이루는 단위
- 각 필드는 특정 종류의 데이터를 저장

# 레코드(record)
: 테이블의 행을 이루는 단위
- 관련된 필드의 데이터를 그룹화하여 저장

*/